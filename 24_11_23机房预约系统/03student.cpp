//    查看自身预约
void Student::showMyOerder(){
    OrderFile of;   //正常实例化对象，可以用
//    OrderFile *of = new OrderFile;//可以用
//    OrderFile(); //可以用

    if(of.m_Size == 0){
        cout<<"无预约记录！"<<endl;
        system("pause");//按任意键继续
        system("cls");//清屏
        return;
    }

    for (int i = 0; i < of.m_Size; ++i) {
//        this->m_Id是int类型
//        of.m_orderData[i]["stuId"]是str类型，无法对比操作
//        后面加一个 .c_str()转换成 c语言风格的字符串 const char*
//        最后利用 atoi的方式(const char*)强制转换成 int 类型
//        这样 == 两边类型一致就可以进行 条件判断
        if(this->m_Id == atoi(of.m_orderData[i]["stuId"].c_str())){//找到自身预约
            cout<<"预约日期：周"<<of.m_orderData[i]["date"];
            cout<<" 时间段："<<(of.m_orderData[i]["interval"]=="1"?"上午":"下午");
            cout<<" 机房编号："<<of.m_orderData[i]["roomId"];

            string status = " 状态";//1审核中  2成功预约 -1预约失败 0取消预约
            if(of.m_orderData[i]["status"] == "1"){
                status += "审核中";
            } else if(of.m_orderData[i]["status"] == "2"){
                status += "成功预约";
            } else if(of.m_orderData[i]["status"] == "-1"){
                status += "预约失败,审核未通过";
            } else if(of.m_orderData[i]["status"] == "0"){
                status += "预约已取消";
            }
            cout<<status<<endl;
        }
    }
    system("pause");//按任意键继续
    system("cls");//清屏
}

