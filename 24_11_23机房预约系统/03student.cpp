//    取消预约
void Student::cancelOrder(){
    OrderFile of;
    if(of.m_Size == 0){
        cout<<"无预约记录"<<endl;
        system("pause");//按任意键继续
        system("cls");//清屏
        return;
    }
    cout<<"审核中或预约成功的记录可以取消，请输入取消的记录"<<endl;

    vector<int>v;//存放在最大容器中的下标编号
    int index = 1;
    for (int i = 0; i < of.m_Size; ++i) {
//        先判断是自身的学号
        if(this->m_Id == atoi(of.m_orderData[i]["stuId"].c_str())){
//            再筛选状态     审核中或预约成功才可以
            if(of.m_orderData[i]["status"] == "1" ||of.m_orderData[i]["status"] == "2"){
                v.push_back(i);
                cout<<index++<<"、";
                cout<<"预约日期：周"<<of.m_orderData[i]["date"];
                cout<<" 时间段："<<(of.m_orderData[i]["interval"]=="1"?"上午":"下午");
                cout<<" 机房编号："<<of.m_orderData[i]["roomId"];

                string status = " 状态：";//1审核中  2成功预约 -1预约失败 0取消预约
                if(of.m_orderData[i]["status"] == "1"){
                    status += "审核中";
                } else if(of.m_orderData[i]["status"] == "2"){
                    status += "成功预约";
                }
//                else if(of.m_orderData[i]["status"] == "-1"){
//                    status += "预约失败,审核未通过";
//                } else if(of.m_orderData[i]["status"] == "0"){1
//                    status += "预约已取消";
//                }
                cout<<status<<endl;
            }
        }
    }

    //index没有++，说明上面的for循环没有进入  也就是没有满足条件的预约记录，审核为空，直接退出到上一级
    if(index == 1){
        cout<<"暂无待取消记录！"<<endl;
        system("pause");//按任意键继续
        system("cls");//清屏
        return;
    }

    cout<<"请输入要取消的记录，0代表返回"<<endl;//没有1这个选项
    int select = 0;
    while (true){
        cin>>select;    //输入选择
        if(select>=0 && select<=v.size()){//3
            if(select == 0){//返回
                break;
            } else{//要取消的记录
                of.m_orderData[v[select-1]]["status"] = "0";//改成 0 取消记录
                of.updateOrder();   //更新文件
                cout<<"已取消预约！"<<endl;
                break;
            }
        }
        cout<<"输入有误，请重新输入！"<<endl;
    }
    system("pause");//按任意键继续
    system("cls");//清屏
}
